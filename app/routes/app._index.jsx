export const action = async ({ request }) => {
  const { admin } = await authenticate.admin(request);
  const color = ["Red", "Orange", "Yellow", "Green"][
    Math.floor(Math.random() * 4)
  ];
  const response = await admin.graphql(
    `#graphql
      query {
      shopifyFunctions(first: 25) {
        nodes {
          app {
            title
          }
          apiType
          appKey
          title
          id
        }
      }
    }`,
  );
  const responseJson = await response.json();
  const shopifyFunctions = responseJson.data.shopifyFunctions?.nodes;

  //finding shopify cart Trasnform function Ids
  if (shopifyFunctions && Array.isArray(shopifyFunctions) && shopifyFunctions.length) {
    for (const fn of shopifyFunctions) {
      if (fn.apiType === 'cart_transform' && fn.appKey === process.env.SHOPIFY_API_KEY) {
        let functionId =  fn.id;
        const response2 = await admin.graphql(
          `#graphql
       mutation {
          cartTransformCreate(functionId: "${functionId}") {
            cartTransform {
              id
              functionId
            }
            userErrors {
              field
              message
            }
          }
        }`,
        );
        const responseJson = await response2.json();
        console.log(responseJson)
      };
    }
  }
// console.log(responseJson.data.shopifyFunctions)
  return json({ });
};



